    text("- 开始符号 $S$")
    text("- 这个文法生成的语言 $L(G)$ 包含所有形式为 $a^n b^n$ 的字符串，其中 $n \\geq 0$。例如：")
    text("- $\\epsilon$（当 $n=0$）")
    text("- $ab$（当 $n=1$）")
    text("- $aabb$（当 $n=2$）")
    text("- $aaabbb$（当 $n=3$）")
