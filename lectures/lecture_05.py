def pushdown_automata():
    text("### 下推自动机")
    text("- 下推自动机（Pushdown Automaton, PDA）是一种比有限自动机更强大的计算模型，可以识别上下文无关语言。")
    text("- 下推自动机由一个七元组 $(Q, \Sigma, \Gamma, \delta, q_0, Z_0, F)$ 组成：")
    text("1. $Q$ 是一个有限状态集合。")
    text("2. $\Sigma$ 是一个有限输入符号集合（字母表）。")
    text("3. $\Gamma$ 是一个有限栈符号集合。")
    text("4. $\delta: Q \times (\Sigma \cup \\{\\epsilon\\}) \times \Gamma \rightarrow \\mathcal{P}(Q \times \\Gamma^*)$ 是一个转移函数，定义了在状态 $q$ 接收输入符号 $a$（或空输入 $\epsilon$）并且栈顶符号为 $X$ 时，可以转移到哪些状态并且对栈进行什么操作。")
    text("5. $q_0 \in Q$ 是初始状态。")
    text("6. $Z_0 \in \Gamma$ 是初始栈符号。")
    text("7. $F \subseteq Q$ 是接受状态集合。")
    text("- 下推自动机的运行：")
    text("1. 初始状态为 $q_0$，初始栈符号为 $Z_0$。")
    text("2. 在每一步，自动机根据当前状态、输入符号（或空输入）和栈顶符号，根据转移函数 $\delta$ 转移到新的状态，并且对栈进行相应的操作（如弹出栈顶符号、推入新的符号等）。")
    text("3. 当输入字符串被完全读取，并且自动机处于接受状态时，输入字符串被接受。")
    text("- 下推自动机可以用来识别上下文无关语言，例如：")
    text("1. 语言 $L = \\{a^n b^n | n \\geq 0\\}$ 可以由一个下推自动机识别，该自动机会在读取 $a$ 时将一个符号推入栈中，在读取 $b$ 时弹出一个符号，最后当输入被完全读取且栈为空时接受。")
    text("2. 语言 $L = \\{a^n b^m c^n | n, m \\geq 0\\}$ 可以由一个下推自动机识别，该自动机会在读取 $a$ 时将一个符号推入栈中，在读取 $b$ 时不对栈进行操作，在读取 $c$ 时弹出一个符号，最后当输入被完全读取且栈为空时接受。")


